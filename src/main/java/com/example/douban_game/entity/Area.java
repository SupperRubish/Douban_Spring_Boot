package com.example.douban_game.entity;

public class Area {
    public int area_id;

    public int north_us;

    public int south_us;

    public int asia;

    public int europe;

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }

    public int getNorth_us() {
        return north_us;
    }

    public void setNorth_us(int north_us) {
        this.north_us = north_us;
    }

    public int getSouth_us() {
        return south_us;
    }

    public void setSouth_us(int south_us) {
        this.south_us = south_us;
    }

    public int getAsia() {
        return asia;
    }

    public void setAsia(int asia) {
        this.asia = asia;
    }

    public int getEurope() {
        return europe;
    }

    public void setEurope(int europe) {
        this.europe = europe;
    }
}
